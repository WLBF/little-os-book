struct registers
{
   uint32_t ds;
   uint32_t edi, esi, ebp, esp, ebx, edx, ecx, eax;
   uint32_t int_no, err_code;
   uint32_t eip, cs, eflags, useresp, ss;
};

typedef void (*isr_t)(struct registers);
void register_interrupt_handler(uint8_t n, isr_t handler);

